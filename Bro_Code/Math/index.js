// Math - An intrinsic object that provides basic mathematics functionality and constants.

// Rounding variable example.
let x = 3.14;
x = Math.round(x);
console.log("Value rounded: " + x);

// Rounding down variable example
let y = 3.99;
y = Math.floor(y);
console.log("Value rounded down: " + y);

// Rounding up variable example.
let z = 3.14;
z = Math.ceil(z);
console.log("Value rounded up: " + z);

// Power variable example.
let w = 3.14;
w = Math.pow(w, 2);
console.log("Power variable: " + w);

// Square root variable example.
let v = 3.14;
v = Math.sqrt(v);
console.log("Square root: " + v);

// Absolute variable example.
let u = -3.14;
u = Math.abs(u);
console.log("Absolute value: " + u);

// Finding the maximum and minimum of three values.
let t = 5;
let s = 9;
let maximum;
let minimum;
maximum = Math.max(u, t, s);
minimum = Math.min(u, t, s);
console.log("Maximum of three values: " + maximum);
console.log("Minimum of three values: " + minimum);

// Displays the full value of PI.
let r = Math.PI;
console.log("Full value of PI: " + r);
