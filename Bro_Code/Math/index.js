// Math - An intrinsic object that provides basic mathematics functionality and constants.

// Rounding variable example.
let x = 3.14;
x = Math.round(x);
console.log("Value rounded: " + x);

// Rounding down variable example
let y = 3.99;
y = Math.floor(x);
console.log("Value rounded down: " + y);

// Rounding up variable example.
let z = 3.14;
z = Math.ceil(z);
console.log("Value rounded up: " + z);
